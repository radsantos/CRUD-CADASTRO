    public void listarTabelaAlunos(){
        
        AlunoDAO alunoDAO = new AlunoDAO();
        
        List<Alunos> lista = alunoDAO.listarAlunos();
        DefaultTableModel dados = (DefaultTableModel) tabelaAlunos.getModel();
        dados.setNumRows(0);
        
        for(Alunos a : lista){
            dados.addRow(new Object[]{
            
            a.getId(),
            a.getMatricula(),
            a.getNome()
        });
        }
        
        
    }
    
    
    
    
