    private void tabelaAlunosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaAlunosMouseClicked
      
       txtId.setText(tabelaAlunos.getValueAt(tabelaAlunos.getSelectedRow(),0).toString());
       txtMatricula.setText(tabelaAlunos.getValueAt(tabelaAlunos.getSelectedRow(),1).toString());
        txtNome.setText(tabelaAlunos.getValueAt(tabelaAlunos.getSelectedRow(),2).toString());
        
        
    }//GEN-LAST:event_tabelaAlunosMouseClicked

